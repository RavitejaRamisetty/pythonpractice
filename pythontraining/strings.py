a=" This is Python "
print(a[1])         #gets the character at the given position


print(a[2:5])       #gets the character fron poition 2 to position 5


print(a.strip())    #strip() method is used to remove the white spaces from beginning to end


print(len(a))       #len() method is used to find the length of the string


print(a.lower())    #lower() method returns the string in lower case


print(a.upper())    #upper() method returns the string in upper case


print(a.replace("i","j"))    #replace() method is used to replace the string with another string